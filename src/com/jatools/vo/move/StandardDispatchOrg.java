package com.jatools.vo.move;

public class StandardDispatchOrg {
	private String dispatchFlag;
	private String coef;
	private String dispMin;
	private String dispMax;
	private String stockNum;
	private String onwayNum;
	private String dispatchNum;
	private String orgId;

	public String getDispatchFlag() {
		return dispatchFlag;
	}

	public void setDispatchFlag(String dispatchFlag) {
		this.dispatchFlag = dispatchFlag;
	}

	public String getCoef() {
		return coef;
	}

	public void setCoef(String coef) {
		this.coef = coef;
	}

	public String getDispMin() {
		return dispMin;
	}

	public void setDispMin(String dispMin) {
		this.dispMin = dispMin;
	}

	public String getDispMax() {
		return dispMax;
	}

	public void setDispMax(String dispMax) {
		this.dispMax = dispMax;
	}

	public String getStockNum() {
		return stockNum;
	}

	public void setStockNum(String stockNum) {
		this.stockNum = stockNum;
	}

	public String getOnwayNum() {
		return onwayNum;
	}

	public void setOnwayNum(String onwayNum) {
		this.onwayNum = onwayNum;
	}

	public String getDispatchNum() {
		return dispatchNum;
	}

	public void setDispatchNum(String dispatchNum) {
		this.dispatchNum = dispatchNum;
	}

	public String getOrgId() {
		return orgId;
	}

	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}

}
