package com.jatools.vo.pur;

public class PurchaseEnter {
	private String enterid;
	private String lineid;
	private String enterType;
	private String numEnter;
	private String outdateFlag;
	private String memo;
	private String createId;
	private String createDate;
	private String status;

	public String getEnterid() {
		return enterid;
	}

	public void setEnterid(String enterid) {
		this.enterid = enterid;
	}

	public String getLineid() {
		return lineid;
	}

	public void setLineid(String lineid) {
		this.lineid = lineid;
	}

	public String getEnterType() {
		return enterType;
	}

	public void setEnterType(String enterType) {
		this.enterType = enterType;
	}

	public String getNumEnter() {
		return numEnter;
	}

	public void setNumEnter(String numEnter) {
		this.numEnter = numEnter;
	}

	public String getOutdateFlag() {
		return outdateFlag;
	}

	public void setOutdateFlag(String outdateFlag) {
		this.outdateFlag = outdateFlag;
	}

	public String getMemo() {
		return memo;
	}

	public void setMemo(String memo) {
		this.memo = memo;
	}

	public String getCreateId() {
		return createId;
	}

	public void setCreateId(String createId) {
		this.createId = createId;
	}

	public String getCreateDate() {
		return createDate;
	}

	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
