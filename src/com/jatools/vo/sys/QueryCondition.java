package com.jatools.vo.sys;

public class QueryCondition {
	private String id;
	private String userid;
	private String queryCode;
	private String relation;
	private String leftQuot;
	private String conditionId;
	private String operIndex;
	private String content;
	private String rightQuot;
	private String sort;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public String getQueryCode() {
		return queryCode;
	}

	public void setQueryCode(String queryCode) {
		this.queryCode = queryCode;
	}

	public String getRelation() {
		return relation;
	}

	public void setRelation(String relation) {
		this.relation = relation;
	}

	public String getLeftQuot() {
		return leftQuot;
	}

	public void setLeftQuot(String leftQuot) {
		this.leftQuot = leftQuot;
	}

	public String getConditionId() {
		return conditionId;
	}

	public void setConditionId(String conditionId) {
		this.conditionId = conditionId;
	}

	public String getOperIndex() {
		return operIndex;
	}

	public void setOperIndex(String operIndex) {
		this.operIndex = operIndex;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getRightQuot() {
		return rightQuot;
	}

	public void setRightQuot(String rightQuot) {
		this.rightQuot = rightQuot;
	}

	public String getSort() {
		return sort;
	}

	public void setSort(String sort) {
		this.sort = sort;
	}

}
