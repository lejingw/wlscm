package com.jatools.vo.bd;

//托架颜色
public class BracketColor {
	private String bracketColorId;
	private String bracketColorDsc;
	private String archiveFlag;//ARCHIVEFLAG
	public String getBracketColorId() {
		return bracketColorId;
	}
	public void setBracketColorId(String bracketColorId) {
		this.bracketColorId = bracketColorId;
	}
	public String getBracketColorDsc() {
		return bracketColorDsc;
	}
	public void setBracketColorDsc(String bracketColorDsc) {
		this.bracketColorDsc = bracketColorDsc;
	}
	public String getArchiveFlag() {
		return archiveFlag;
	}
	public void setArchiveFlag(String archiveFlag) {
		this.archiveFlag = archiveFlag;
	}
	
	
}
