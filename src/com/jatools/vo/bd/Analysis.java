package com.jatools.vo.bd;

public class Analysis {
	private String analysisId;
	private String analysisDesc;
	private String analysisStart;
	private String analysisEnd;
	
	public String getAnalysisId() {
		return analysisId;
	}
	public void setAnalysisId(String analysisId) {
		this.analysisId = analysisId;
	}
	public String getAnalysisDesc() {
		return analysisDesc;
	}
	public void setAnalysisDesc(String analysisDesc) {
		this.analysisDesc = analysisDesc;
	}
	public String getAnalysisStart() {
		return analysisStart;
	}
	public void setAnalysisStart(String analysisStart) {
		this.analysisStart = analysisStart;
	}
	public String getAnalysisEnd() {
		return analysisEnd;
	}
	public void setAnalysisEnd(String analysisEnd) {
		this.analysisEnd = analysisEnd;
	}
	
}
